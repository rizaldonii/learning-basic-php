<?php

echo "Decimal : ";
var_dump(1234); // Decimal number

echo "Octal : ";
var_dump(01234); // Octal number

echo "Hexadecimal : ";
var_dump(0x1A); // Hexadecimal number

echo "Binary : ";
var_dump(0b111111111); // Binary number

echo "Underscore in number : ";
var_dump(1_234_234); // Number with underscore

echo "Floating Point : ";
var_dump(1.234); // Floating Point

echo "Floating Point dengan E notation Plus (1.2 x 1000) : ";
var_dump(1.2e3); // Floating Point with E notation Plus

echo "Floating Point dengan E notation Minus (7 x 0.001) : ";
var_dump(7e-3); // Floating Point with E notation Minus

echo "Underscore di Floating Point : ";
var_dump(1_234.567); // Floating Point with underscore

echo "Integer Overflow 32-bit : ";
var_dump(2147483648); // Integer Overflow 32-bit

echo "Integer Overflow 64-bit : ";
var_dump(9223372036854775807); // Integer Overflow 64-bit